using System;

namespace Uppgift_4._1
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 10; i <= 30; i++)
            {
                Console.WriteLine(i);
            }

            for (int a = 200; a >= 180; a--)
            {
                Console.WriteLine(a);
            }

            for (int b = 1000; b <= 1400; b=b+50)
            {
                Console.WriteLine(b);
            }

        }
    }


}